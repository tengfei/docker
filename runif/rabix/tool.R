library(rabix)
rbx <- RabixTool(id = "runif",
                 label = "Random number generator",
                 description = "Random number generator",
                 dockerPull = "tengfei/runif",
                 cpu = 1, mem = 1024,
                 baseCommand = "runif.R",
                 stdout = "out.txt",
                 inputs = list(input(id = "number",
                     description = "number of observations",
                     type = "integer",
                     label = "number",
                     prefix = "--n",
                     default = 1),
                     input(id = "min",
                           description = "lower limits of the distribution",
                           type = "float",
                           label = "min",
                           prefix = "--min",
                           default = 0),
                     input(id = "max",
                           description = "upper limits of the distribution",
                           type = "float",
                           label = "max",
                           prefix = "--max",
                           default = 1),
                     input(id = "seed",
                           description = "seed with set.seed",
                           type = "float",
                           label = "seed",
                           prefix = "--seed",
                           default = 1)),
                 outputs = list(
                     output(id = "random_file",
                            type = "file",
                            label = "output", 
                            description = "random number file",
                            glob = "*.txt"),
                     output(id = "report",
                            type = "file",
                            label = "report_output",
                            description = "report",
                            glob = "*.html")))
rbx$toJSON(pretty = TRUE)
